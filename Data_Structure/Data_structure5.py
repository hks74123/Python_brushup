# String

# Creation
word = "HEMANT"
print(word)

# String operations
print(word.lower())
print(word.upper())
print(len(word))
print(word.startswith("K"))
print(word.endswith("T"))


# Slicing the string
print(word[0:5])

# Coverting string to a list of characters
print(list(word))

# splitting the text
sentence = "I love python learning"
words = sentence.split(" ")
print(words)

# finding length of a list
print(len(words))